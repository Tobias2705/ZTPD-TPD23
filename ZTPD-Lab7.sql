-- Ćwiczenie 1A
CREATE TABLE A6_LRS (
    GEOM SDO_GEOMETRY
);

-- Ćwiczenie 1B
INSERT INTO A6_LRS
SELECT SR.GEOM
FROM STREETS_AND_RAILROADS SR
WHERE SR.ID = (
    SELECT SR.ID
    FROM STREETS_AND_RAILROADS SR, MAJOR_CITIES M
    WHERE SDO_RELATE(SR.GEOM,SDO_GEOM.SDO_BUFFER(M.GEOM, 10, 1, 'unit=km'), 'mask=ANYINTERACT') = 'TRUE'
    AND M.CITY_NAME = 'Koszalin'
);

-- Ćwiczenie 1C
SELECT 
    SDO_GEOM.SDO_LENGTH(A6.GEOM, 1, 'unit=km') AS DISTANCE,
    ST_LINESTRING(A6.GEOM).ST_NUMPOINTS() AS ST_NUMPOINTS
FROM A6_LRS A6;

-- Ćwiczenie 1D
UPDATE A6_LRS A6
SET GEOM = SDO_LRS.CONVERT_TO_LRS_GEOM(A6.GEOM, 0, 276.681315399186);

